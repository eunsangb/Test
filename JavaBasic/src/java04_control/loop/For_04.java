package java04_control.loop;

public class For_04 {

	public static void main(String[] args) {
		
		//	"Hello"를 7번 출력하시오
		
		//반복횟수 : 7번
		//반복구간 : 0 ~ 6
		
		//초기식 : 	(i변수 선언, 반복 구간의 시작값으로 대입, 주로 0)
		//조건식 :  (i변수 검사, 반복횟수보다 작은 조건으로 설정)
		//증감식 : 	(1씩증가)
		
		for(int i=0; i<7; i++) {
			System.out.println("Hello");
		}
		
		
	}

}
